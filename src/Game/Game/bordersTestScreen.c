	case 93:
		return 30;
		break;
	case 95:
		return 31;
		break;
	case 111:
		return 32;
		break;
	case 127:
		return 33;
		break;
	case 134:
		return 34;
		break;
	case 135:
		return 35;
		break;
	case 142:
		return 36;
